package com.br.transform;

/**
 * ValueGeneratorConfigItem.
 * Holds configuration for value generator processors
 *
 */

public class ValueGeneratorConfigItem {

    private String name = "";

    private String value = "";

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
